    <section class="sections" id="missions">
        <h2>Nos missions</h2>
        <div>
            <p>Notre mission est de fournir un refuge sûr et accueillant &agrave; ceux qui en ont besoin, tout en les accompagnant
                dans leur parcours vers une vie stable et &eacute;panouie. Nous offrons un h&eacute;bergement temporaire, garantissant un
                espace s&eacute;curis&eacute; et respectueux o&ugrave; chaque individu peut se sentir valoris&eacute;.
                <br>
                <br>
                Notre engagement ne s&apos;arr&ecirc;te pas l&agrave;. Nous proposons &eacute;galement un suivi personnalis&eacute; pour aider nos r&eacute;sidents
                &agrave; construire un avenir meilleur. Cela inclut l&apos;accompagnement dans les d&eacute;marches administratives, le soutien
                &eacute;ducatif et professionnel, ainsi que le soutien psychologique et &eacute;motionnel.
                <br>
                <br>
                Nous croyons que l&apos;h&eacute;bergement est le premier pas vers la s&eacute;curit&eacute;, et notre soutien continu permet &agrave; chacun
                de reprendre confiance et de se reconstruire durablement.</p>
